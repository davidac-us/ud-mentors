const DICTS: Record<Lang, Dict> = { en, es, pt, zh, ar, fr };

type Ctx = { lang: Lang; setLang: (l: Lang) => void; t: (key: string) => string };

const I18nContext = createContext<Ctx>({
  lang: "en",
  setLang: () => {},
  t: (k) => k,
});

function readStoredLang(): Lang {
  try {
    const v = localStorage.getItem("mm_lang") as Lang | null;
    if (v && DICTS[v]) return v;
  } catch {}
  return "en";
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");

  useEffect(() => {
    setLangState(readStoredLang());
    const onStorage = (e: StorageEvent) => {
      if (e.key === "mm_lang") setLangState(readStoredLang());
    };
    window.addEventListener("storage", onStorage);
    return () => window.removeEventListener("storage", onStorage);
  }, []);

  useEffect(() => {
    if (typeof document !== "undefined") {
      document.documentElement.lang = lang;
      document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
    }
  }, [lang]);

  const setLang = useCallback((l: Lang) => {
    try { localStorage.setItem("mm_lang", l); } catch {}
    setLangState(l);
  }, []);

  const t = useCallback(
    (key: string) => DICTS[lang]?.[key] ?? en[key] ?? key,
    [lang]
  );

  return <I18nContext.Provider value={{ lang, setLang, t }}>{children}</I18nContext.Provider>;
}

export function useT() {
  return useContext(I18nContext);
}
